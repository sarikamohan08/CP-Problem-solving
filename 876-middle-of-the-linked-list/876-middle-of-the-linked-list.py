# Definition for singly-linked list.
# class ListNode(object):
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
class Solution(object):
    def middleNode(self, head):
        """
        :type head: ListNode
        :rtype: ListNode
        """
        tmp=head
        prev=head
        while tmp and tmp.next:
            tmp=tmp.next.next
            prev=prev.next
        
        return prev